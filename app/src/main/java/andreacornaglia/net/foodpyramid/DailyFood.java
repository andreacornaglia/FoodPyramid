package andreacornaglia.net.foodpyramid;

import java.util.Date;

public class DailyFood {
	
	private int mGrainServing;
	private int mVegServing;
	private int mFruitServing;
	private int mDairyServing;
	private int mProteinServing;
	private int mOthers;
	private Date mDate;
	
	public int getmGrainServing() {
		return mGrainServing;
	}
	public void setmGrainServing(int mGrainServing) {
		this.mGrainServing = mGrainServing;
	}
	public int getmVegServing() {
		return mVegServing;
	}
	public void setmVegServing(int mVegServing) {
		this.mVegServing = mVegServing;
	}
	public int getmFruitServing() {
		return mFruitServing;
	}
	public void setmFruitServing(int mFruitServing) {
		this.mFruitServing = mFruitServing;
	}
	public int getmDairyServing() {
		return mDairyServing;
	}
	public void setmDairyServing(int mDairyServing) {
		this.mDairyServing = mDairyServing;
	}
	public int getmProteinServing() {
		return mProteinServing;
	}
	public void setmProteinServing(int mProteinServing) {
		this.mProteinServing = mProteinServing;
	}
	public int getmOthers() {
		return mOthers;
	}
	public void setmOthers(int mOthers) {
		this.mOthers = mOthers;
	}
	public Date getmDate() {
		return mDate;
	}
	public void setmDate(Date mDate) {
		this.mDate = mDate;
	}
	
	

}
